<template>
  <div class="card">
    <div class="card-image">
      <figure class="image is-4by3">
        <nuxt-link :to="link">
          <img :src="backendURL + ''" alt="Product name" />
        </nuxt-link>
      </figure>
    </div>
    <div class="card-content">
      <div class="media">
        <div class="media-content">
          <nuxt-link :to="link" class="title is-5">
            {{ product.name }}
          </nuxt-link>
        </div>
      </div>

      <div class="content">
        <p v-if="product.description">
          {{ product.description }}
        </p>

        <span class="tag is-primary is-rounded is-medium">{{
          product.price
        }}</span>
      </div>
    </div>
  </div>
</template>

<script>
export default {
  props: {
    product: {
      type: Object,
      required: true,
    },
  },

  computed: {
    link() {
      return {
        name: 'products-slug',
        params: {
          slug: this.product.slug,
        },
      };
    },
    backendURL() {
      return process.env.backendURL;
    },
  },
};
</script>
